#!/bin/bash

# Задаём массивы с размерами для эксперимента:
# n - число строк первой матрицы, m - число столбцов первой (и строк второй), p - число столбцов второй матрицы.
n_values=(256 512 1024 2048)
m_values=(128 256 512 1024)
p_values=(64 128 256 512)

# Режимы: 
# 0 - матрицы без транспонирования,
# 1 - транспонирование первой матрицы,
# 2 - транспонирование второй матрицы,
# 3 - транспонирование обеих матриц.
modes=(0 1 2 3)

# Количество повторений для каждого набора параметров
repeats=5

# Путь к бинарному файлу (предполагается, что он находится в текущей директории)
binary="./build/Debug/matmul.exe"

# Параметр patchstep (обычно равен 1)
patchstep=1

# Перебираем массивы по индексам (так как размеры подбираются по соответствию)
for i in "${!n_values[@]}"; do
    n="${n_values[$i]}"
    m="${m_values[$i]}"
    p="${p_values[$i]}"
    echo "Размеры: n=$n, m=$m, p=$p"
    for mode in "${modes[@]}"; do
        echo "  Режим (caseKernel) = $mode"
        for (( run=1; run<=repeats; run++ )); do
            # Формируем имя файла для логирования результатов
            logfile="results_1/n${n}_m${m}_p${p}_mode${mode}_run${run}.txt"
            echo "    Запуск $run, лог: $logfile"
            # Запускаем программу с заданными параметрами
            $binary $n $m $p $patchstep $mode > "$logfile"
        done
        echo "  -----------------------------"
    done
    echo "====================================="
done
