#!/bin/bash

# Задаем массив размерностей. Здесь тестируем квадратные матрицы.
sizes=(256 512 1024 2048)
# Количество повторений для каждого эксперимента
repeats=5

# Режимы: 0 - без транспонирования, 1 - транспонирование первой, 2 - второй, 3 - обе
modes=(0 1 2 3)

for n in "${sizes[@]}"; do
    m=$n
    p=$n
    echo "Размер матриц: n = $n, m = $m, p = $p"
    for mode in "${modes[@]}"; do
        echo "   Режим (caseKernel) = $mode"
        for (( i=1; i<=repeats; i++ )); do
            # Запускаем программу. Предположим, что бинарник называется matmulMain
            ./build/Debug/matmul.exe $n $m $p 1 $mode > "results/n${n}_m${m}_p${p}_mode${mode}_run${i}.txt"
            echo "      Запуск $i завершен"
        done
        echo "   -----------------------------"
    done
    echo "====================================="
done
